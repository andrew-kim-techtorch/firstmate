#!/usr/bin/env bash
# shellcheck shell=bash
# Detect and repair no-mistakes post-receive gate hooks pinned on the buggy
# bare "$(pwd)" gate-path pattern.
#
# A no-mistakes gate hook (~/.no-mistakes/repos/<id>.git/hooks/post-receive)
# used to resolve its own directory with the bare `$(pwd)` shell builtin.
# Pushing from a linked git worktree (every crewmate ship task) leaves $PWD as
# "." during receive-pack, so the daemon rejects the push with "invalid gate
# path: ." and no validation run is ever created - the crewmate silently
# stalls. The fix, `$(pwd)` -> `$(pwd -P)`, resolves via the getcwd() syscall
# and ignores the possibly-"." $PWD. This is an upstream no-mistakes bug
# (regenerated by `no-mistakes init` and by version upgrades that overwrite
# the hook); this lib is firstmate's defensive self-heal, not the root fix.

# gate_hook_is_bad <hook-file>: true if the hook still contains the bare
# "$(pwd)" pattern. An already-patched hook using "$(pwd -P)" does not match,
# since that substring is "$(pwd " followed by a flag, never "$(pwd)".
gate_hook_is_bad() {
  local hook=$1
  [ -f "$hook" ] || return 1
  # shellcheck disable=SC2016  # single quotes are intentional: literal string, no shell expansion
  grep -qF '$(pwd)' "$hook"
}

# gate_hook_repair <hook-file>: rewrite every bare "$(pwd)" to "$(pwd -P)" in
# place. Idempotent: once no bare "$(pwd)" remains, gate_hook_is_bad is false
# and this is never called again. Returns 1 on write failure.
gate_hook_repair() {
  local hook=$1 tmp mode
  tmp="$hook.fm-repair.tmp.$$"
  mode=$(stat -f '%Lp' "$hook" 2>/dev/null || stat -c '%a' "$hook" 2>/dev/null || echo 755)
  # shellcheck disable=SC2016  # single quotes are intentional: literal sed pattern, no shell expansion
  if sed 's/\$(pwd)/$(pwd -P)/g' "$hook" > "$tmp"; then
    chmod "$mode" "$tmp" && mv "$tmp" "$hook"
  else
    rm -f "$tmp"
    return 1
  fi
}

# gate_repo_for_project <project-dir>: echo the local no-mistakes bare gate
# repo path for a project clone, or return 1 if it has no no-mistakes remote
# (direct-PR/local-only projects, or a no-mistakes project not yet initialized).
gate_repo_for_project() {
  local proj=$1 url
  url=$(git -C "$proj" remote get-url no-mistakes 2>/dev/null) || return 1
  [ -n "$url" ] || return 1
  printf '%s\n' "$url"
}
